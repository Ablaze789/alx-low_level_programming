#include "calc.h"

/**
 *	op_add - This function sums two integer
 *	@a: first integer
 *	@b: second integer
 *	Return: Integer
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 *	op_sub - This function subtracts two integer
 *	@a: first integer
 *	@b: second integer
 *	Return: Integer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 *	op_mul - This function multiplies two integer
 *	@a: first integer
 *	@b: second integer
 *	Return: Integer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 *	op_div - This function divides two integer
 *	@a: first integer
 *	@b: second integer
 *	Return: Integer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 *	op_mod - This function performs modulus on two integer
 *	@a: first integer
 *	@b: second integer
 *	Return: Integer
 */

int op_mod(int a, int b)
{
	return (a % b);
}
